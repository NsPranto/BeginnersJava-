package data_type;

public class DataType
{
    public static void main(String[] args) 
    {
        boolean b=true; //dynamic initialization
        char c='a'; //dynamic initialization
        short s=32677; //dynamic initialization
        double d=50.1; //dynamic initialization
        int i=10; //dynamic initialization
        float f=10.5f; //dynamic initialization
        
        System.out.println("b = "+b);
        System.out.println("c = "+c);
        System.out.println("s = "+s);
        System.out.println("d = "+d);
        System.out.println("i = "+i);
        System.out.println("f = "+f);
        
    }
}
